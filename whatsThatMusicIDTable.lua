local function resolveKey(key, specialist)
    specialist = specialist or false
    
    if specialist then
        -- Using SoundtrackNames to check if Soundtrack Menu is active, since the mod variable is LOCAL >:(
        if antibirthmusicplusplus and SoundtrackNames then
            return 604
        elseif antibirthmusicplusplus then
            return 240
        elseif SoundtrackNames then
            return 567
        else
            return 203
        end
    end
    if not antibirthmusicplusplus then return key else return key + 37 end
end

